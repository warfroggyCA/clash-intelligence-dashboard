/**
 * Format weekly summary for Discord
 */
export function formatWeeklySummaryForDiscord(data: {
  totalMembers: number;
  activeMembers: number;
  totalDonations: number;
  avgTrophies: number;
  warWins?: number;
  warLosses?: number;
  topDonors: Member[];
  topTrophyGainers: Member[];
}): string {
  let message = `${discord.emoji.chart} ${discord.bold('Weekly Clan Summary')} ${discord.emoji.chart}\n\n`;
  
  message += `${discord.bold('Clan Stats:')}\n`;
  message += `${discord.bullet} Total Members: ${data.totalMembers}\n`;
  message += `${discord.bullet} Active Members: ${data.activeMembers}\n`;
  message += `${discord.bullet} Total Donations: ${data.totalDonations.toLocaleString()}\n`;
  message += `${discord.bullet} Avg Trophies: ${Math.round(data.avgTrophies).toLocaleString()}\n`;
  
  if (data.warWins !== undefined && data.warLosses !== undefined) {
    const total = data.warWins + data.warLosses;
    const winRate = total > 0 ? ((data.warWins / total) * 100).toFixed(1) : '0.0';
    message += `${discord.bullet} War Record: ${data.warWins}W - ${data.warLosses}L (${winRate}%)\n`;
  }
  
  message += '\n';
  
  if (data.topDonors.length > 0) {
    message += `${discord.emoji.gift} ${discord.bold('Top Donors:')}\n`;
    data.topDonors.slice(0, 3).forEach((member, i) => {
      message += `${discord.bullet} ${i + 1}. ${member.name} - ${member.donations?.toLocaleString() || 0} donations\n`;
    });
    message += '\n';
  }
  
  if (data.topTrophyGainers.length > 0) {
    message += `${discord.emoji.trophy} ${discord.bold('Top Trophy Pushers:')}\n`;
    data.topTrophyGainers.slice(0, 3).forEach((member, i) => {
      message += `${discord.bullet} ${i + 1}. ${member.name} - ${member.trophies?.toLocaleString() || 0} trophies\n`;
    });
    message += '\n';
  }
  
  message += `${discord.italic('Generated by Clash Intelligence Dashboard')}`;
  return message;
}

/**
 * Format a war result summary for Discord
 */
export function formatWarResultForDiscord(payload: WarResultPayload): string {
  const scoreline = `${payload.ourStars}-${payload.opponentStars}`;
  const percentLine =
    payload.ourPercent != null && payload.opponentPercent != null
      ? ` (${payload.ourPercent.toFixed(1)}% vs ${payload.opponentPercent.toFixed(1)}%)`
      : '';
  const outcome =
    payload.result === 'win'
      ? `${discord.emoji.trophy} Victory`
      : payload.result === 'loss'
        ? `${discord.emoji.warning} Defeat`
        : `${discord.emoji.chart} Draw`;

  let message = `${discord.bold(`${payload.clanName} vs ${payload.opponentName}`)}\n`;
  message += `${discord.bullet(`Result: ${outcome} ${discord.code(`${scoreline}${percentLine}`)}`)}\n`;
  message += `${discord.bullet(`Our Tag: ${discord.code(payload.clanTag)} â€¢ Opponent Tag: ${discord.code(payload.opponentTag)}`)}\n`;
  if (payload.warType) {
    message += `${discord.bullet(`War Type: ${payload.warType}`)}\n`;
  }
  if (payload.warId) {
    message += `${discord.bullet(`War ID: ${discord.code(payload.warId)}`)}\n`;
  }
  message += '\n';

  if (payload.mvp) {
    const { name, tag, stars, summary } = payload.mvp;
    const parts = [stars != null ? `${stars}â˜…` : null, summary].filter(Boolean).join(' â€¢ ');
    message += `${discord.emoji.star} ${discord.bold('MVP')}: ${name}${tag ? ` ${discord.code(tag)}` : ''}${parts ? ` â€” ${parts}` : ''}\n`;
  }

  if (payload.topPerformers?.length) {
    message += `\n${discord.bold('Top Performers')}\n`;
    payload.topPerformers.forEach((perf, index) => {
      const parts = [perf.stars != null ? `${perf.stars}â˜…` : null, perf.summary].filter(Boolean).join(' â€¢ ');
      message += `${discord.bullet(`${index + 1}. ${perf.name}${perf.tag ? ` ${discord.code(perf.tag)}` : ''}${parts ? ` â€” ${parts}` : ''}`)}\n`;
    });
  }

  if (payload.bravest) {
    const { name, tag, stars, summary } = payload.bravest;
    const desc = [summary, stars != null ? `${stars}â˜…` : null].filter(Boolean).join(' â€¢ ');
    message += `\n${discord.bold('Bravest Attack')}: ${name}${tag ? ` ${discord.code(tag)}` : ''}${desc ? ` â€” ${desc}` : ''}\n`;
  }

  if (payload.learnings?.length) {
    message += `\n${discord.bold('Learnings')}\n`;
    payload.learnings.forEach((learning) => {
      message += `${discord.bullet(`${learning.title}: ${learning.detail}`)}\n`;
    });
  }

  message += `\n${discord.italic('Generated by Clash Intelligence Dashboard')}`;
  return message;
}

/**
 * Copy text to clipboard
 */
export async function copyToClipboard(text: string): Promise<boolean> {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch (error) {
    console.error('Failed to copy to clipboard:', error);
    return false;
  }
}

/**
 * Convert data to CSV format
 */
export function toCSV(headers: string[], rows: string[][]): string {
  const csvRows = [headers.join(',')];
  
  rows.forEach(row => {
    const escaped = row.map(cell => {
      // Escape quotes and wrap in quotes if contains comma
      const cellStr = String(cell);
      if (cellStr.includes(',') || cellStr.includes('"') || cellStr.includes('\n')) {
        return `"${cellStr.replace(/"/g, '""')}"`;
      }
      return cellStr;
    });
    csvRows.push(escaped.join(','));
  });
  
  return csvRows.join('\n');
}

/**
 * Export elder candidates to CSV
 */
export function exportElderCandidatesToCSV(candidates: Member[]): string {
  const headers = ['Name', 'Tag', 'Role', 'Trophies', 'Donations', 'War Stars', 'Capital Gold', 'Town Hall'];
  const rows = candidates.map(member => [
    member.name,
    member.tag,
    member.role || 'member',
    String(member.trophies || 0),
    String(member.donations || 0),
    String(member.warStars || 0),
    String(member.clanCapitalContributions || 0),
    String(member.townHallLevel || member.th || 0),
  ]);
  
  return toCSV(headers, rows);
}

/**
 * Export watchlist to CSV
 */
export function exportWatchlistToCSV(watchlist: { member: Member; reason: string; severity: string }[]): string {
  const headers = ['Name', 'Tag', 'Severity', 'Reason', 'Trophies', 'Donations', 'Last Activity'];
  const rows = watchlist.map(item => [
    item.member.name,
    item.member.tag,
    item.severity,
    item.reason,
    String(item.member.trophies || 0),
    String(item.member.donations || 0),
    'N/A', // Would need activity tracking
  ]);
  
  return toCSV(headers, rows);
}

/**
 * Download CSV file
 */
export function downloadCSV(filename: string, csvContent: string): void {
  const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
  const link = document.createElement('a');
  const url = URL.createObjectURL(blob);
  
  link.setAttribute('href', url);
  link.setAttribute('download', filename);
  link.style.visibility = 'hidden';
  
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
}
